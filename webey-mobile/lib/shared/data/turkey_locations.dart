  /// API şehir filtresi için ASCII/legacy yazımları kanonik TR isme çevirir.
  static String? normalizeCityForApi(String? raw) {
    if (raw == null) return null;
    final trimmed = raw.trim();
    if (trimmed.isEmpty) return null;

    const aliases = <String, String>{
      'istanbul': 'İstanbul',
      'Istanbul': 'İstanbul',
      'ISTANBUL': 'İstanbul',
      'izmir': 'İzmir',
      'Izmir': 'İzmir',
      'ankara': 'Ankara',
    };
    if (aliases.containsKey(trimmed)) return aliases[trimmed];

    for (final city in cities) {
      if (city.toLowerCase() == trimmed.toLowerCase()) return city;
    }
    return trimmed;
  }

  /// İlçe adını şehir listesindeki kanonik yazıma yaklaştırır (ör. Uskudar → Üsküdar).
  static String? normalizeDistrictForApi(String? city, String? raw) {
    if (raw == null) return null;
    final trimmed = raw.trim();
    if (trimmed.isEmpty) return null;

    final normalizedCity = normalizeCityForApi(city);
    if (normalizedCity != null) {
      for (final district in districtsFor(normalizedCity)) {
        if (district.toLowerCase() == trimmed.toLowerCase()) return district;
      }
    }

    if (normalizedCity == 'İstanbul') {
      const aliases = <String, String>{
        'uskudar': 'Üsküdar',
        'Uskudar': 'Üsküdar',
        'kadikoy': 'Kadıköy',
        'Kadikoy': 'Kadıköy',
        'umraniye': 'Ümraniye',
        'Umraniye': 'Ümraniye',
      };
      if (aliases.containsKey(trimmed)) return aliases[trimmed];
      final lower = trimmed.toLowerCase();
      if (aliases.containsKey(lower)) return aliases[lower]!;
    }

    return trimmed;
  }
