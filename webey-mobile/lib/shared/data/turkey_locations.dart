  static List<String> districtsFor(String city) {
    final districts = districtsByCity[city];
    if (districts != null && districts.isNotEmpty) return districts;
    return const ['Merkez'];
  }

  static List<String> neighborhoodsFor(String city, String district) {
    final perCity = _neighborhoodsByCityDistrict[city];
    if (perCity == null) return const [];
    final list = perCity[district];
    return list ?? const [];
  }

